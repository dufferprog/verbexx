#define EXCEP_MSG_BUF_LEN 10000

// --------------------------------------------------------------------------------------------------------
// local helper function for generating text for exception.code() -- note: multi-line output -- contains \n
// --------------------------------------------------------------------------------------------------------

static std::wstring error_code_string(const std::wstring& ws, const std::error_code& ec)
{
    wchar_t buf[EXCEP_MSG_BUF_LEN + 2] { };  

    (void)_snwprintf_s( buf
                      , EXCEP_MSG_BUF_LEN
                      , _TRUNCATE 
                      , L"std::%25s -- .code().category().name()                           = \"%s\" \n" 
                        L"std::%25s -- .code().value()                                     = %d     \n"    
                        L"std::%25s -- .code().message()                                   = \"%s\" \n"
                        L"std::%25s -- .code().default_error_condition().category().name() = \"%s\" \n"
                        L"std::%25s -- .code().default_error_condition().value()           = %d     \n"    
                        L"std::%25s -- .code().default_error_condition().message()         = \"%s\" \n"
                      , ws.c_str(), to_wstring(ec.category().name()).c_str()
                      , ws.c_str(),            ec.value() 
                      , ws.c_str(), to_wstring(ec.message()).c_str()
                      , ws.c_str(), to_wstring(ec.default_error_condition().category().name()).c_str()
                      , ws.c_str(),            ec.default_error_condition().value() 
                      , ws.c_str(), to_wstring(ec.default_error_condition().message()).c_str()
                      );
                                                                             
    return std::wstring { buf }; 
}


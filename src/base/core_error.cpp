// local helper function for printing out error_code fields -- cout mutex should be held across this functon call
// --------------------------------------------------------------------------------------------------------------

static void error_code_message(const wchar_t *s,  const std::error_code& ec)
{
    M_out_emsg0(L"std::%s -- .code().category().name()                           = \"%s\""     ) % s % to_wstring(ec.category().name()                            ); 
    M_out_emsg0(L"std::%s -- .code().value()                                     = %d"         ) % s %            ec.value(); 
    M_out_emsg0(L"std::%s -- .code().message()                                   = \"%s\""     ) % s % to_wstring(ec.message()                                    ); 
    M_out_emsg0(L"std::%s -- .code().default_error_condition().category().name() = \"%s\""     ) % s % to_wstring(ec.default_error_condition().category().name()  ); 
    M_out_emsg0(L"std::%s -- .code().default_error_condition().value()           = %d"         ) % s %            ec.default_error_condition().value(); 
    M_out_emsg2(L"std::%s -- .code().default_error_condition().message()         = \"%s\""     ) % s % to_wstring(ec.default_error_condition().message()          );   
    
    return; 
}


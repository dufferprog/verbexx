void setup_exception(bool set_sig)
{
    set_terminate(                   &handle_terminate   ); 
    set_unexpected(                  &handle_unexpected  );
    _set_se_translator(              &handle_se          );
    _set_invalid_parameter_handler(  &handle_invalid_parm);


    // ••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••
    // Set up to catch all available ANSI signals, if requested (i.e. running on IPT)
    // ••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••••       
    
    if (set_sig)
    {
        signal(SIGSEGV , &handle_signal);
        signal(SIGILL  , &handle_signal);
        signal(SIGFPE  , &handle_signal);
        signal(SIGTERM , &handle_signal);
        signal(SIGINT  , &handle_signal);
        signal(SIGABRT , &handle_signal);
    }

    return; 
}





